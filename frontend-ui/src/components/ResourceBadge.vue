<template>
  <v-chip size="small" :density="density" :variant="variant" class="mr-2">
    <v-icon :icon="icon" size="small" class="mr-1" />
    {{ displayedValue }}
  </v-chip>
</template>

<script setup lang="ts">
import { formattedBytesSize } from '@/utils/format'
import { computed } from 'vue'

// Props
interface Props {
  kind: 'cpu' | 'memory' | 'disk' | 'shm'
  value: number
  humanValue?: string
  density?: 'compact' | 'default'
  variant?: 'outlined' | 'text' | 'tonal' | 'plain' | 'elevated' | 'flat'
}

const props = withDefaults(defineProps<Props>(), {
  humanValue: undefined,
  density: 'default',
  variant: 'outlined',
})

// Computed properties
const displayedValue = computed(() => {
  if (props.humanValue) {
    return props.humanValue
  }
  return props.kind === 'cpu' ? props.value : formattedBytesSize(props.value)
})

const icon = computed(() => {
  const iconMap = {
    cpu: 'mdi-cpu-64-bit',
    memory: 'mdi-memory',
    disk: 'mdi-harddisk',
    shm: 'mdi-disc',
  }
  return iconMap[props.kind]
})
</script>
